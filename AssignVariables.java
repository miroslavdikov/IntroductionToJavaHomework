public class AssignVariables {
    public static void main(String[] args) {
        boolean booleanType= false;
        String stringType= "Palo Alto, CA";
        short shortType= 32767;
        int intType= 2000000000;
        double doubleType= 0.1234567891011d;
        float floatType= 0.5f;
        long longType= 919827112351L;
        byte byteType= 127;
        char charType= 'c';

        System.out.println(booleanType);
        System.out.println(stringType);
        System.out.println(shortType);
        System.out.println(intType);
        System.out.println(doubleType);
        System.out.println(floatType);
        System.out.println(longType);
        System.out.println(byteType);
        System.out.println(charType);

    }
}
